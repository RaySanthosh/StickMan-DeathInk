/// Level definitions.
///
/// Grid legend:
///   `#` solid block      `.` empty
///   `S` player start     `E` exit door
///   `C` checkpoint       `^` spikes
///   `s` saw blade        `X` crusher (hangs from the tile above)
///   `~` vanishing platform
///   `L` laser emitter (beam fires straight down)
///   `!` pop-up spikes (hidden; faint red specks on the floor are the hint)
///   `d` dart shooter (inkwell doodle; its muzzle flashes red before firing)
///   `F` fake floor (crumbles underfoot; the hairline crack is the hint)
///   `v` hanging barbs (kill standing players; slide underneath them)
///   `H` rope (auto-grab while falling; hold JUMP to climb, SLIDE to descend)
///   `w` rail saw (rides around the edge of the platform it's attached to)
///
/// Rows are written as adjacent 10-char string chunks so widths stay exact.
class LevelData {
  const LevelData({
    required this.name,
    required this.world,
    required this.parSeconds,
    required this.rows,
  });

  final String name;
  final int world; // 1-based world number
  final double parSeconds;
  final List<String> rows;

  int get cols => rows.first.length;
  int get rowCount => rows.length;
}

